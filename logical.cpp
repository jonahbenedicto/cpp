#include <iostream>
using namespace std;

int main() {
    int a = 6, b = 4;

    // Logical AND operator
    cout << "AND" << endl;
    cout << "a && b is " << (a && b) << endl;
  
    // Logical OR operator
    cout << "OR" << endl;
    cout << "a || b is " << (a || b) << endl;
  
    // Logical NOT operator
    cout << "NOT" << endl;
    cout << "!b is " << (!b);

    return 0;
}
